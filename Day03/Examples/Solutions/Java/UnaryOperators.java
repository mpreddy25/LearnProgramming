public class UnaryOperators{

     public static void main(String []args){
        int i = 5;
        i++;
        System.out.println(i);    // "6"
        ++i;                     
        System.out.println(i);    // "7"
        System.out.println(++i);  // "8"
        System.out.println(i++);  // "8"
        System.out.println(i);    // "9"
     }
}